	<h2>WIN WIN x Food/drinks</h2>
	<p>Wednesday, 4 March 2020, 18:00 - 20:30</p>
	<p>Hello friends!</p>
	<p><b>Nienke and I are inviting you for Chinese food and drinks along side critical feedback
		and user testing of the current iteration of our WIN WIN project.</b></p>
	<p>WIN WIN was developed in Spring 2019 as part of the Sandberg@Mediapark masterclass as participatory performance where a consensus algorithm is experienced by 2 - 15 people for 10 minutes.</p>

	<p>The masterclass edition was framed by the theme artificial imagination: how imagination can save our future. WIN WIN addresses the current divisive and polarizing (online) discourses in our society by exposing moments of consensus in conversations on complicated subjects. Even though we have endless streams of information and databases of knowledge at our disposal, we have reached a point in time where we find it hard to agree on something and move forward. We designed WIN WIN to assist a human discussion with an algorithmic process, to discover common ground in discussion.</p>

	<p>WIN WIN is supported by the Creative Industries Fund NL (Stimuleringsfonds).</p>
	<p>The evening will happen <b>next week, Wednesday, March 4th, 18:00 - 20:30</b> @ the new community space "Pubkey" (pubkey.space), <b>Korte Zoutkeetsgracht 2, Bel #2, 1013MC Amsterdam.</b> Feel free to bring friends! <b>Please RSVP</b>, so we know how much food to order.</p>
